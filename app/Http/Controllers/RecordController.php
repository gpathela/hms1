    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create($id)
    {
        $patient = \App\Patient::find($id);
        $doctors = \App\Employee::where('role', 2)->get();
        $nurses = \App\Employee::where('role', 3)->get();
        return view('admin/createrecord', compact(['doctors', 'nurses', 'patient'])); 
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $record = new \App\Record;
        $record->patient = $request->patient;
        $record->doctor = $request->doctor;
        $record->nurse = $request->nurse;
        $record->disease = $request->disease;
        $record->save();
        return redirect('admin');
    }

